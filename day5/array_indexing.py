#    -9  -8  -7  -6  -5  -4  -3  -2  -1
a = [11, 22, 33, 44, 55, 66, 77, 88, 99]
#     0   1   2   3   4   5   6   7   8

print("a                              = ", a[:])
print("a[2:]                          = ", a[2:])
print("a[2:7]                         = ", a[2:7])
print("a[2:8:2]                       = ", a[2:8:2])
print("a[8:2:2]                       = ", a[8:2:2])
print("a[::-1]                        = ", a[::-1])
print("a[2:-2]                        = ", a[2:-2])
print("a[1::-2]                       = ", a[1::-2])
print("a[1::-1]                       = ", a[1::-1])
print("a[-1:2:-1]                     = ", a[-1:2:-1])